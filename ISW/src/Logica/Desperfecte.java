package Logica;

public class Desperfecte {
	//Atributs
	private String zona;
	private String descripcio;
	
	//Getters & Setters
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	public String getDescripcio() {
		return descripcio;
	}
	public void setDescripcio(String descripcio) {
		this.descripcio = descripcio;
	}
}
